     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        /** @var \App\Models\Link $link */
        $link = $request->user()->links()->create([
            'code' => Str::random(6),
        ]);

        $link->products()->sync($request->products);

        return $link;
    }

